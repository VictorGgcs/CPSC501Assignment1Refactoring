package human;

public class CreateHuman {
	Head head;
	Torso torso;
	ArmLeft armLeft;
	ArmRight armRight;
	LegLeft legLeft;
	LegRight legRight;
	
	
	
	
	public CreateHuman() {
		this.head = new Head();
		this.torso = new Torso();
		this.armLeft = new ArmLeft();
		this.armRight = new ArmRight();
		this.legLeft = new LegLeft();
		this.legRight = new LegRight();
	}
	
	

}
